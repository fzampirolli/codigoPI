# DEFINIÇÕES DOS MÉTODOS

def calcDist(x, y):
    resposta = (x**2 + y**2) ** 0.5
    return resposta

# PROGRAMA PRINCIPAL

print(calcDist(1, 1), calcDist(2, 2))
